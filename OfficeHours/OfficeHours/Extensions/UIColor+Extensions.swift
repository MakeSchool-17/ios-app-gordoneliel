import UIKit

extension UIColor {
    static func primaryBlueColor() -> UIColor {
        return UIColor(red: 3/255, green: 129/255, blue: 201/255, alpha: 1.0)
    }
    
    static func lighterBlueColor() -> UIColor {
        return UIColor(red:0.09, green:0.6, blue:0.92, alpha:1)
    }
    
    static func deepRed() -> UIColor {
        return UIColor(red: 238/255, green: 57/255, blue: 48/255, alpha: 1.0)
    }
    
    static func orangeMelon() -> UIColor {
        return UIColor(red: 232/255, green: 175/255, blue: 20/255, alpha: 1.0)
    }
    
    static func lightBlueColor() -> UIColor {
        return UIColor(red: 29/255, green: 173/255, blue: 255/255, alpha: 1.0)
    }
    
    static func lighterDarkGrayColor() -> UIColor {
        return UIColor(red: 71/255, green: 71/255, blue: 71/255, alpha: 1.0)
    }
    
    static func lighterGrayColor() -> UIColor {
        return UIColor(red: 230/255, green: 230/255, blue: 230/255, alpha: 1.0)
    }
}